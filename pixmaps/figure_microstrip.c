/* array of pointers to the widths and heights */
int figure_microstrip_width[1];
int figure_microstrip_height[1];

/* This function should be called at the beginning of the user program */
void figure_microstrip_init(void)
{

  figure_microstrip_total_pages = 1;

  figure_microstrip_render[0] = figure_microstrip_page_1_render;

  figure_microstrip_width[0] = figure_microstrip_page_1_width;
  figure_microstrip_height[0] = figure_microstrip_page_1_height;
}

float figure_microstrip_width_max = 206;
float figure_microstrip_height_max = 103;
