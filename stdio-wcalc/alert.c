#include "config.h"

#include <stdarg.h>
#include <stdio.h>
#include <stdlib.h>
#ifdef HAVE_STRING_H
#include <string.h>
#endif

#include "alert.h"

#ifdef DMALLOC
#include <dmalloc.h>
#endif

void alert(const char *fmt,...)
{
  va_list ap;


  /* extract the warning string */
  va_start(ap,fmt);
  vfprintf(stderr,fmt,ap);
  va_end(ap);
}




